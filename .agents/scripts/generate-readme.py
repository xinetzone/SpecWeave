"""目录 README 自动生成与索引更新工具。

批量为缺失 README.md 的内容聚合目录生成标准化入口文档，支持两种策略：
  - Strategy B（模板生成）：扫描当前目录下的 .md 文件，提取 frontmatter 元数据，
    生成索引表 + 子目录导航
  - Strategy C（自动索引）：递归扫描子目录，生成层级索引结构
  - 增量更新（--update）：重新扫描目录，更新已有 README 中标记区域内的索引表，
    保留用户自定义的概述、相关资源等手动编辑内容；自动升级旧版无标记 README

用法：
  python generate-readme.py --scan                     # 仅扫描缺失 README 的目录
  python generate-readme.py --target-dir <dir>         # 为指定目录生成 README
  python generate-readme.py --p1                       # 批量处理 P1 阶段内容聚合目录
  python generate-readme.py --all                      # 处理所有缺失 README 的目录
  python generate-readme.py --update                   # 增量更新所有脚本生成的 README 索引
  python generate-readme.py --update --target-dir <dir> # 更新指定目录的 README 索引
  python generate-readme.py --update --dry-run         # 预览索引更新
  python generate-readme.py --target-dir <dir> --force # 覆盖已存在的 README
"""

