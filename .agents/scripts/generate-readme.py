def generate_for_directory(
    dir_path: Path,
    root: Path,
    dry_run: bool = False,
    force: bool = False,
    custom_title: str | None = None,
) -> tuple[bool, str]:
    readme_path = dir_path / 'README.md'

    if readme_path.exists() and not force:
        return False, f"已存在，跳过（使用 --force 覆盖）"

    doc_entries, dir_entries = scan_directory(dir_path, root)

    if not doc_entries and not dir_entries:
        return False, "目录为空，跳过"

    content = generate_readme_content(dir_path, root, doc_entries, dir_entries, custom_title)

    if dry_run:
        rel = dir_path.relative_to(root)
        print()
        print_header(f"预览: {rel / 'README.md'}")
        print(content)
        return True, "预览完成"

    readme_path.write_text(content, encoding='utf-8')
    return True, f"已生成（{len(doc_entries)}个文档，{len(dir_entries)}个子目录）"


def find_generated_readmes(root: Path, start_path: Path | None = None) -> list[Path]:
    search_root = start_path or (root / 'docs')
    results = []
    for readme in sorted(search_root.rglob('README.md')):
        parts = set(readme.parts)
        if EXCLUDED_DIRS & parts:
            continue
        try:
            content = readme.read_text(encoding='utf-8')
        except (OSError, UnicodeDecodeError):
            continue
        if GENERATED_FOOTER_PREFIX in content:
            results.append(readme)
    return results


def upgrade_readme_markers(readme_path: Path, root: Path) -> bool:
    dir_path = readme_path.parent
    content = readme_path.read_text(encoding='utf-8')

    if README_INDEX_START in content and README_INDEX_END in content:
        return False

    doc_entries, dir_entries = scan_directory(dir_path, root)
    index_tables = generate_index_tables(doc_entries, dir_entries)

    marker_block = f"{README_INDEX_START}\n{index_tables}\n{README_INDEX_END}"

    doc_idx_pos = content.find("## 📄 文档索引")
    dir_nav_pos = content.find("## 📁 子目录导航")
    resources_pos = content.find("## 🔗 相关资源")

    if doc_idx_pos != -1 and resources_pos != -1:
        new_content = (
            content[:doc_idx_pos]
            + marker_block
            + "\n\n"
            + content[resources_pos:]
        )
        readme_path.write_text(new_content, encoding='utf-8')
        return True

    if doc_idx_pos == -1 and dir_nav_pos == -1 and resources_pos != -1:
        new_content = (
            content[:resources_pos]
            + marker_block
            + "\n\n"
            + content[resources_pos:]
        )
        readme_path.write_text(new_content, encoding='utf-8')
        return True

    return False


def update_readme_index(
    readme_path: Path,
    root: Path,
    dry_run: bool = False,
) -> tuple[bool, str]:
    if not readme_path.exists():
        return False, "README 不存在"

    content = readme_path.read_text(encoding='utf-8')

    has_markers = README_INDEX_START in content and README_INDEX_END in content
    has_footer = GENERATED_FOOTER_PREFIX in content

    if not has_markers:
        if has_footer:
            if dry_run:
                return True, "需要升级标记（将添加START/END标记）"
            upgraded = upgrade_readme_markers(readme_path, root)
            if not upgraded:
                return False, "无法自动升级标记区域（未找到插入位置）"
            content = readme_path.read_text(encoding='utf-8')
        else:
            return False, "非脚本生成的README，跳过"

    dir_path = readme_path.parent
    doc_entries, dir_entries = scan_directory(dir_path, root)
    new_index = generate_index_tables(doc_entries, dir_entries)

    if not new_index.strip():
        new_index = "_暂无文档_"

    start_marker_end = content.find(README_INDEX_START) + len(README_INDEX_START)
    end_marker_start = content.find(README_INDEX_END)
    old_index = content[start_marker_end:end_marker_start].strip()

    if old_index == new_index.strip():
        return True, f"索引已是最新（{len(doc_entries)}个文档，{len(dir_entries)}个子目录）"

    if dry_run:
        rel = dir_path.relative_to(root)
        print()
        print_header(f"将更新: {rel / 'README.md'}")
        print(f"  文档: {len(doc_entries)}个, 子目录: {len(dir_entries)}个")
        return True, f"将更新（{len(doc_entries)}个文档，{len(dir_entries)}个子目录）"

    update_marker_region(readme_path, README_INDEX_START, README_INDEX_END, new_index)

    today = date.today().isoformat()
    updated_content = readme_path.read_text(encoding='utf-8')
    old_date_pattern = re.compile(
        r'<!-- generated by generate-readme\.py on \d{4}-\d{2}-\d{2} -->'
    )
    new_footer = f"<!-- generated by generate-readme.py on {today} -->"
    if old_date_pattern.search(updated_content):
        updated_content = old_date_pattern.sub(new_footer, updated_content, count=1)
    else:
        updated_content = updated_content.rstrip() + "\n\n" + new_footer + "\n"
    readme_path.write_text(updated_content, encoding='utf-8')

    return True, f"已更新（{len(doc_entries)}个文档，{len(dir_entries)}个子目录）"


def cmd_update(args) -> int:
    root = args.path or resolve_project_root(__file__)

    targets = []
    if args.target_dir:
        tp = Path(args.target_dir).resolve()
        if tp.is_file() and tp.name == 'README.md':
            targets = [tp]
        elif tp.is_dir():
            targets = [tp / 'README.md']
        else:
            print_error(f"目标路径无效: {args.target_dir}")
            return 1
    else:
        targets = find_generated_readmes(root)
        print(f"更新模式：发现 {len(targets)} 个由脚本生成的 README")

    success = 0
    skipped = 0
    failed = 0
    upgraded = 0

    for readme in targets:
        try:
            dir_path = readme.parent
            ok, msg = update_readme_index(readme, root, dry_run=args.dry_run)
            try:
                rel = dir_path.relative_to(root)
            except ValueError:
                rel = dir_path
            if ok:
                if "升级" in msg or "添加" in msg:
                    upgraded += 1
                print_pass(f"  {rel}: {msg}")
                success += 1
            else:
                print_warn(f"  {rel}: {msg}")
                skipped += 1
        except Exception as e:
            print_error(f"  {readme}: 失败 - {e}")
            failed += 1

    print()
    if args.dry_run:
        print(f"预览完成：{success} 个将更新，{skipped} 个跳过，{failed} 个失败"
              + (f"，{upgraded} 个需要升级标记" if upgraded else ""))
    else:
        print(f"执行完成：{success} 个成功，{skipped} 个跳过，{failed} 个失败"
              + (f"，{upgraded} 个已升级标记" if upgraded else ""))

    return 0 if failed == 0 else 1


def cmd_scan(args) -> int:
    root = args.path or resolve_project_root(__file__)
    missing = find_missing_readmes(root, min_md_files=args.min_files)
    print(f"扫描结果：共发现 {len(missing)} 个目录缺失 README（含至少 {args.min_files} 个.md文件）")
    print()
    for d in missing:
        rel = d.relative_to(root)
        md_count = len(list(d.glob('*.md')))
        subdirs = len([x for x in d.iterdir() if x.is_dir() and not x.name.startswith('.')])
        print(f"  {rel} - {md_count}个md, {subdirs}个子目录")
    return 0


def cmd_generate(args) -> int:
    root = args.path or resolve_project_root(__file__)

    targets = []
    if args.target_dir:
        targets = [Path(args.target_dir).resolve()]
    elif args.p1:
        targets = get_p1_dirs(root)
        print(f"P1 阶段：共 {len(targets)} 个待处理目录")
    elif args.all:
        targets = find_missing_readmes(root, min_md_files=args.min_files)
        print(f"全部模式：共 {len(targets)} 个待处理目录")
    else:
        print_error("请指定目标：--path <dir> / --p1 / --all / --scan")
        return 1

    success = 0
    skipped = 0
    failed = 0

    for target in targets:
        try:
            ok, msg = generate_for_directory(
                target, root,
                dry_run=args.dry_run,
                force=args.force,
                custom_title=args.title,
            )
            try:
                rel = target.relative_to(root)
            except ValueError:
                rel = target
            if ok:
                print_pass(f"  {rel}: {msg}")
                success += 1
            else:
                print_warn(f"  {rel}: {msg}")
                skipped += 1
        except Exception as e:
            print_error(f"  {target}: 失败 - {e}")
            failed += 1

    print()
    if args.dry_run:
        print(f"预览完成：{success} 个目录将生成，{skipped} 个跳过，{failed} 个失败")
    else:
        print(f"执行完成：{success} 个成功，{skipped} 个跳过，{failed} 个失败")

    return 0 if failed == 0 else 1


def main():
    setup_safe_output()
    parser = argparse.ArgumentParser(description='目录 README 自动生成与索引更新工具')
    parser.add_argument('--path', type=Path, default=None, help='项目根目录路径（默认自动解析）')
    parser.add_argument('--dry-run', action='store_true', help='预览模式，不写入文件')
    parser.add_argument('--force', action='store_true', help='强制覆盖已存在的 README')
    parser.add_argument('--title', type=str, default=None, help='自定义目录标题（单目录模式下使用）')
    parser.add_argument('--min-files', type=int, default=1, help='最小md文件数（仅对--all/--scan生效）')

    group = parser.add_mutually_exclusive_group(required=False)
    group.add_argument('--scan', action='store_true', help='仅扫描缺失 README 的目录')
    group.add_argument('--p1', action='store_true', help='批量处理 P1 阶段内容聚合目录')
    group.add_argument('--all', action='store_true', help='处理所有缺失 README 的目录')
    group.add_argument('--update', action='store_true', help='增量更新已有README的索引表（自动升级旧标记）')
    parser.add_argument('--target-dir', type=str, default=None,
                        help='指定目标目录（生成模式为该目录生成README；更新模式配合--update更新该目录）')

    args = parser.parse_args()

    has_action = args.scan or args.update or args.p1 or args.all or args.target_dir
    if not has_action:
        parser.print_help()
        return 1

    if args.scan:
        return cmd_scan(args)
    elif args.update:
        return cmd_update(args)
    else:
        return cmd_generate(args)


if __name__ == "__main__":
    sys.exit(main())
